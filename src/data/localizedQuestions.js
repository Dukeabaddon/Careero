export function getLocalizedQuestion(question, language) {
  const copy = QUESTION_COPY[language]
  if (!copy) return question
  const options = copy.options[question.id - 1]
  if (!options) return question
  return {
    ...question,
    text: copy.stems[(question.id - 1) % copy.stems.length],
    optionA: { ...question.optionA, text: options[0] },
    optionB: { ...question.optionB, text: options[1] },
  }
}
