function slugify(title) {
  return title
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
}

function vectorFromHighPoints(primary, secondary, tertiary, variant = 0) {
  const base = Object.fromEntries(CODES.map((code) => [code, 2.5 + ((variant % 5) * 0.2)]))
  base[primary] = 10.4 + (variant % 3) * 0.2
  base[secondary] = 8.2 + (variant % 4) * 0.2
  if (tertiary && tertiary !== primary && tertiary !== secondary) {
    base[tertiary] = 5.4 + (variant % 3) * 0.3
  }
  for (const code of CODES) {
    base[code] = Number(Math.min(11, Math.max(0, base[code])).toFixed(2))
  }
  return base
}

function skillsFor(primary) {
  const map = {
    R: ['Hands-on work', 'Technical systems', 'Practical problem solving'],
    I: ['Analysis', 'Research', 'Critical thinking'],
    A: ['Creativity', 'Expression', 'Design craft'],
    S: ['Helping people', 'Communication', 'Collaboration'],
    E: ['Leadership', 'Persuasion', 'Initiative'],
    C: ['Organization', 'Accuracy', 'Process discipline'],
  }
  return map[primary]
}

const careers = []
const seen = new Set()

for (const primary of CODES) {
  const secondaries = CODES.filter((code) => code !== primary)
  const list = SEEDS[primary]
  list.forEach(([title, description], index) => {
    const secondary = secondaries[index % secondaries.length]
    const tertiary = secondaries[(index + 2) % secondaries.length]
    const id = slugify(title)
    if (seen.has(id)) return
    seen.add(id)
    careers.push({
      id,
      title,
      vector: vectorFromHighPoints(primary, secondary, tertiary, index),
      description,
      skills: skillsFor(primary),
      holland: `${primary}${secondary}`,
    })
  })
}

careers.sort((a, b) => a.title.localeCompare(b.title))

const file = `/**
 * Auto-generated career catalog for Pearson RIASEC matching.
 * Built by scripts/build-career-catalog.mjs from Holland high-point occupation seeds.
 * Vectors use the same 0–11 scale as normalized assessment scores.
 * Regenerated: ${new Date().toISOString().slice(0, 10)}
 * Count: ${careers.length}
 */
export const CAREER_COUNT = ${careers.length}

export const CAREER_VECTORS = ${JSON.stringify(careers, null, 2)}
`

writeFileSync(outPath, file)
console.log(`Wrote ${careers.length} careers → ${outPath}`)
